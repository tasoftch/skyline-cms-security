namespace Skyline\CMS\Security\Tool\Event;


use Skyline\CMS\Security\UserSystem\Role;
use TASoft\EventManager\Event\Event;

class UpdateRoleEvent extends Event
{
    /** @var Role */
    private $role;
    /** @var string|null */
    private $name;
    /** @var string|null */
    private $description;
    /** @var int|null */
    private $options;
    /** @var Role|null */
    private $parentRole;

    /**
     * @return Role
     */
    public function getRole(): Role
    {
        return $this->role;
    }

    /**
     * @param Role $role
     */
    public function setRole(Role $role): void
    {
        $this->role = $role;
    }

    /**
     * @return string|null
     */
    public function getName(): ?string
    {
        return $this->name;
    }

    /**
     * @param string|null $name
     */
    public function setName(?string $name): void
    {
        $this->name = $name;
    }

    /**
     * @return string|null
     */
    public function getDescription(): ?string
    {
        return $this->description;
    }

    /**
     * @param string|null $description
     */
    public function setDescription(?string $description): void
    {
        $this->description = $description;
    }

    /**
     * @return int|null
     */
    public function getOptions(): ?int
    {
        return $this->options;
    }

    /**
     * @param int|null $options
     */
    public function setOptions(?int $options): void
    {
        $this->options = $options;
    }

    /**
     * @return Role|null
     */
    public function getParentRole(): ?Role
    {
        return $this->parentRole;
    }

    /**
     * @param Role|null $parentRole
     */
    public function setParentRole(?Role $parentRole): void
    {
        $this->parentRole = $parentRole;
    }
}